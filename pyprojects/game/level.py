class Tile(pygame.sprite.Sprite):
    def __init__(self, pos: tuple, size: int, color: tuple) -> None:
        pygame.sprite.Sprite.__init__(self)

        self.image = pygame.Surface((size, size))
        self.image.fill(color)
        self.rect = self.image.get_rect(topleft=pos)

class Level:
    def __init__(self, initial_level_map: str) -> None:
        self.current_level_map: str = initial_level_map
        self.tiles = pygame.sprite.Group()
    
    def fill_level_tiles(self):
        """
        Creates group of sprites and assigns is to Level().tiles,
        This function serves the purpose of Creating the layout initially, or if you want to create or update new layout
        """
        self.tiles.empty()
        for i, row in enumerate(level_1):
            for j, col in enumerate(row):
                if col:
                    self.tiles.add( Tile( (j * tile_size, i * tile_size), tile_size, (128, 128, 128) ) )

